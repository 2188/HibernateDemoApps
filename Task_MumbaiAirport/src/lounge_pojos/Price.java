package lounge_pojos;

public class Price {
	
	private int peopleCatTypeId;
	private float price;
	private int loungeId;
	
	public int getPeopleCatTypeId() {
		return peopleCatTypeId;
	}
	public void setPeopleCatTypeId(int peopleCatTypeId) {
		this.peopleCatTypeId = peopleCatTypeId;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public int getLoungeId() {
		return loungeId;
	}
	public void setLoungeId(int loungeId) {
		this.loungeId = loungeId;
	}
}
